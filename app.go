func (a *App) addStoreProducts(w http.ResponseWriter, r *http.Request) {
	vars := mux.Vars(r)
	id, err := strconv.Atoi(vars["id"])
	if err != nil {
		respondWithError(w, http.StatusBadRequest, "Invalid Store ID")
		return
	}

	s := store{ID: id}
	exists, err:= s.checkStoreExistence(a.DB)
	if err != nil {
		respondWithError(w, http.StatusInternalServerError, err.Error())
		return
	}
	if !exists {
		respondWithError(w, http.StatusNotFound, "Store does not exist")
		return
	}

	sp := storeProduct{StoreId: id}
	products := make([]product,0)
	decoder := json.NewDecoder(r.Body)
	if err := decoder.Decode(&products); err != nil {
		respondWithError(w, http.StatusBadRequest, "Invalid request payload")
		return
	}

	if products, err = sp.addStoreProducts(a.DB, products); err != nil {
		respondWithError(w, http.StatusInternalServerError, err.Error())
		return
	}

	respondWithJSON(w, http.StatusCreated, products)
}

